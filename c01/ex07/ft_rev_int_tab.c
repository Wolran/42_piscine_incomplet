void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	j;

	j = 0;
	while (j < --size + 0.5)
	{
		ft_swap(&tab[j], &tab[size]);
		j++;
	}
}
